
public class Release {
	int ID;
	String name;
	
	public Release(int ID, String name) {
		this.ID = ID;
		this.name = name;
	}
	
	public void setID(int ID) {
		this.ID = ID;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getID() {
		return ID;
	}
	
	public int getName() {
		return getName();
	}
}
